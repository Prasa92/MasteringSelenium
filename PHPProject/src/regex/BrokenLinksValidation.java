package regex;

public class BrokenLinksValidation {

	public static void main(String[] args) {
		
		int a[]= {2,5,6,5,8};
		int b[] = a.clone();
		
		for(int i=0; i<a.length;i++){
			System.out.println(a[i] + " ");
		}
		for(int j=0; j<b.length;j++){
			System.out.println(b[j] + " ");
		}	
	}
}
